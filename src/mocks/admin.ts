export function getMockAdminStats(): AdminStats {
  const now = new Date();
  const oneDayAgo = new Date(now.getTime() - 24 * 60 * 60 * 1000).toISOString();
  const sevenDaysAgo = new Date(now.getTime() - 7 * 24 * 60 * 60 * 1000).toISOString();

  const roleDistribution: Record<string, number> = {
    super_admin: 0,
    admin: 0,
    support_admin: 0,
    user: 0,
  };
  for (const u of MOCK_USERS) {
    roleDistribution[u.role] = (roleDistribution[u.role] || 0) + 1;
  }

  return {
    totalUsers: MOCK_USERS.length,
    activeUsers: MOCK_USERS.filter((u) => u.isActive && !u.isSuspended).length,
    disabledUsers: MOCK_USERS.filter((u) => !u.isActive).length,
    lockedUsers: MOCK_USERS.filter((u) => u.isLocked).length,
    suspendedUsers: MOCK_USERS.filter((u) => u.isSuspended).length,
    recentLogins24h: MOCK_SYSTEM_ACTIVITY.filter(
      (a) => a.type === 'login_success' && a.timestamp >= oneDayAgo
    ).length,
    failedLogins24h: MOCK_SYSTEM_ACTIVITY.filter(
      (a) => a.type === 'login_failure' && a.timestamp >= oneDayAgo
    ).length,
    adminActions7d: MOCK_ADMIN_LOGS.filter(
      (l) => l.timestamp >= sevenDaysAgo
    ).length,
    roleDistribution: roleDistribution as AdminStats['roleDistribution'],
  };
}
